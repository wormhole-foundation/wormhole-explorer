func GetUnlockedAmount(date *time.Time) Supply {
	initialDate := Supplies[0].Day                            // Assuming the first entry in the array is the earliest date
	daysDifference := int(date.Sub(initialDate).Hours() / 24) // Calculate the difference in days

	if daysDifference < 0 {
		// If the date is before the initial date, return 0
		return Supply{}
	} else if daysDifference >= len(Supplies) {
		// If the date is beyond the last date, return the last unlocked amount
		return Supplies[len(Supplies)-1]
	}

	// Return the unlocked amount for the calculated index
	return Supplies[daysDifference]
}
